pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
    let len: i32 = count.iter().sum();
    let min = count.iter().position(|x| *x > 0).unwrap();
    let max = count.len() - 1 - count.iter().rev().position(|x| *x > 0).unwrap();
    let sum: f64 = count.iter().enumerate().map(|(i, v)| i as f64 * *v as f64).sum();
    let mean = sum / len as f64;
    let mut iter = 0;
    let mut median = 0.;
    if len & 1 == 1 {
        for i in 0..count.len() {
            iter += count[i];
            if iter > len / 2 {
                median = i as f64;
                break;
            }
        }
    } else {
        let mut a = None;
        let mut b = None;
        for i in 0..count.len() {
            if iter <= len / 2 {
                if iter + count[i] > len / 2 {
                    if a.is_none() {
                        a = Some(i);
                    }
                    b = Some(i);
                    break;
                } else if iter + count[i] == len / 2 && a.is_none() {
                    a = Some(i);
                }
            }
            iter += count[i];
        }
        median = (a.unwrap() + b.unwrap()) as f64 / 2.;
    }
    let mode = (0..count.len()).max_by_key(|x| count[*x]).unwrap();
    vec![min as f64, max as f64, mean, median, mode as f64]
}

