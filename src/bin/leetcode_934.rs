//! 最短的桥

use std::collections::VecDeque;

pub fn shortest_bridge(mut grid: Vec<Vec<i32>>) -> i32 {
    const DIR: [(i32, i32); 4] = [(-1, 0), (0, -1), (1, 0), (0, 1)];
    let len = grid.len();
    let mut idx = 2;
    for i in 0..len {
        for j in 0..len {
            if grid[i][j] == 1 {
                let mut q = VecDeque::new();
                q.push_back((i, j));
                grid[i][j] = idx;
                while !q.is_empty() {
                    let (x, y) = q.pop_front().unwrap();
                    for (dx, dy) in DIR {
                        let (nx, ny) = (x as i32 + dx, y as i32 + dy);
                        if nx >= 0 && nx < len as i32 && ny >= 0 && ny < len as i32 {
                            let (nx, ny) = (nx as usize, ny as usize);
                            if grid[nx][ny] == 1 {
                                grid[nx][ny] = idx;
                                q.push_back((nx, ny));
                            }
                        }
                    }
                }
                idx += 1;
            }
        }
    }
    let mut q = VecDeque::new();
    let mut vis = vec![vec![false; len]; len];
    for i in 0..len {
        for j in 0..len {
            if grid[i][j] == 2 {
                q.push_back((i, j, 0));
                vis[i][j] = true;
            }
        }
    }
    while !q.is_empty() {
        let (x, y, step) = q.pop_front().unwrap();
        if grid[x][y] == 3 {
            return step - 1;
        }
        for (dx, dy) in DIR {
            let (nx, ny) = (x as i32 + dx, y as i32 + dy);
            if nx >= 0 && nx < len as i32 && ny >= 0 && ny < len as i32 {
                let (nx, ny) = (nx as usize, ny as usize);
                if !vis[nx][ny] {
                    q.push_back((nx, ny, step + 1));
                    vis[nx][ny] = true;
                }
            }
        }
    }
    0
}

